package com.geniu.string;

public class switchDemoByte {
    public static void main(String[] args) {
        byte str = 1;
        switch (str) {
            case 1:
                System.out.println("hello");
                break;
            case 2:
                System.out.println("world");
                break;
            default:
                break;
        }
    }
}
